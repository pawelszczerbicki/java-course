package com.company.conditions;

public class Switch {

    public static void main(String[] args) {
        int myChoice = 1;

        switch(myChoice){
            case 1:
                System.out.println("wybralem 1");
                break;
            case 2:
                System.out.println("wybralem 2");
                break;
            default:
                System.out.println("wybralem cos innego");
        }
    }
}
